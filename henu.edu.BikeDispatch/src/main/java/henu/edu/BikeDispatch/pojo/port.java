package henu.edu.BikeDispatch.pojo;

public class port {
    private Integer ID;
    private Integer bikeCount;
    private Integer dist;
    private Integer needBikeCount;
    private Double jindu;
    private Double weidu;

    public Integer getID() {
        return ID;
    }

    public void setID(Integer ID) {
        this.ID = ID;
    }

    public Integer getBikeCount() {
        return bikeCount;
    }

    public void setBikeCount(Integer bikeCount) {
        this.bikeCount = bikeCount;
    }

    public Integer getDist() {
        return dist;
    }

    public void setDist(Integer dist) {
        this.dist = dist;
    }

    public Integer getNeedBikeCount() {
        return needBikeCount;
    }

    public void setNeedBikeCount(Integer needBikeCount) {
        this.needBikeCount = needBikeCount;
    }

    public Double getJindu() {
        return jindu;
    }

    public void setJindu(Double jindu) {
        this.jindu = jindu;
    }

    public Double getWeidu() {
        return weidu;
    }

    public void setWeidu(Double weidu) {
        this.weidu = weidu;
    }
}
